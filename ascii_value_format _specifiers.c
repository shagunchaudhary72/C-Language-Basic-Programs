#include<stdio.h>
int main(void)
{
    printf(" ascii value of letter n %d",'n'); //110
    printf("\n ascii value of new line character \\n : %d",'\n'); //10
    printf("\n ascii value of back space character : %d",'\b');  //8
    printf("\n ascii value of tab space character : %d",'\t');  //9
    printf("\n ascii value of carriage return character : %d",'\r');  //13

    return 0;
}